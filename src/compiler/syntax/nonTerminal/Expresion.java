package compiler.syntax.nonTerminal;

public class Expresion extends NonTerminal {
	
	private String id;
	
	/*Constructor vacío*/
	public Expresion() {
		super();
	}
	
	/*Constructor utilizando id*/
	public Expresion(String id) {
		super();
		this.id = id;
	}

	/*Getter de id*/
	public String getId() {
		return id;
	}
	

	
	

}
